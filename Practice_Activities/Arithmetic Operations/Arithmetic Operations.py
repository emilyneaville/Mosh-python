print(10 / 3)
# // returns an integer instead of float
print(10 // 3)
# % returns the remainder of the division
print(10 % 3)
# ** indicates power, so 10 to the power of 3
print(10 ** 3)

x = 10
x = x + 3
print(x)

# Augmented assignment operator is += or -=
x = 10
x += 3
print(x)
# x = x + 3 is exactly the same as x += 3

x = 10
x *= 3
print(x)

x = 10
x //= 3
print(x)
